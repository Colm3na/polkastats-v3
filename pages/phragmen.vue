<template>
  <div>
    <section>
      <b-container class="page-phragmen main pt-4">
        <h1 class="text-center mb-4">
          {{ $t("pages.phragmen.predicted_candidates_by_TEXT") }}
        </h1>
        <template v-if="enabled">
          <!-- Filter -->
          <b-row>
            <b-col lg="12" class="mb-4">
              <b-form-input
                id="filterInput"
                v-model="filter"
                type="search"
                :placeholder="$t('pages.phragmen.search_placeholder')"
              />
            </b-col>
          </b-row>
          <!-- Mobile sorting -->
          <div class="row d-block d-sm-block d-md-block d-lg-none d-xl-none">
            <b-col lg="6" class="my-1">
              <b-form-group
                :label="$t('pages.phragmen.sort')"
                label-cols-sm="3"
                label-align-sm="right"
                label-size="sm"
                label-for="sortBySelect"
                class="mb-4"
              >
                <b-input-group size="sm">
                  <b-form-select
                    id="sortBySelect"
                    v-model="sortBy"
                    :options="sortOptions"
                    class="w-75"
                  >
                    <template v-slot:first>
                      <option value="">
                        -- none --
                      </option>
                    </template>
                  </b-form-select>
                  <b-form-select
                    v-model="sortDesc"
                    size="sm"
                    :disabled="!sortBy"
                    class="w-25"
                  >
                    <option :value="false">
                      Asc
                    </option>
                    <option :value="true">
                      Desc
                    </option>
                  </b-form-select>
                </b-input-group>
              </b-form-group>
            </b-col>
          </div>
          <!-- Table with sorting and pagination-->
          <div>
            <b-table
              id="candidates-table"
              stacked="md"
              head-variant="dark"
              :fields="fields"
              :items="candidates"
              :per-page="perPage"
              :current-page="currentPage"
              :sort-by.sync="sortBy"
              :sort-desc.sync="sortDesc"
              :filter="filter"
              :filter-included-fields="filterOn"
              @filtered="onFiltered"
            >
              <template slot="rank" slot-scope="data">
                <p class="text-right mb-0">
                  {{ data.item.rank }}
                </p>
              </template>
              <template slot="pub_key_stash" slot-scope="data">
                <div
                  class="d-block d-sm-block d-md-none d-lg-none d-xl-none text-center"
                >
                  <div v-if="hasIdentity(data.item.pub_key_stash)">
                    <div
                      v-if="getIdentity(data.item.pub_key_stash).logo !== ''"
                    >
                      <img
                        :src="getIdentity(data.item.pub_key_stash).logo"
                        class="identity mt-2"
                      />
                    </div>
                  </div>
                  <div v-else>
                    <Identicon
                      :key="data.item.pub_key_stash"
                      :value="data.item.pub_key_stash"
                      :size="80"
                      :theme="'polkadot'"
                    />
                  </div>
                  <h4
                    v-if="hasIdentity(data.item.pub_key_stash)"
                    class="mt-2 mb-2"
                  >
                    {{ getIdentity(data.item.pub_key_stash).full_name }}
                  </h4>
                  <h4
                    v-else-if="hasKusamaIdentity(data.item.pub_key_stash)"
                    class="mt-2 mb-2"
                  >
                    {{ hasKusamaIdentity(data.item.pub_key_stash).display }}
                  </h4>
                  <p class="mt-2 mb-2 rank">rank #{{ data.item.rank }}</p>
                  <p
                    v-b-tooltip.hover
                    class="bonded mb-0"
                    title="$t('pages.phragmen.total_stake')"
                  >
                    {{ formatAmount(data.item.stake_total) }}
                  </p>
                </div>
                <div class="d-none d-sm-none d-md-block d-lg-block d-xl-block">
                  <div
                    v-if="hasIdentity(data.item.pub_key_stash)"
                    class="d-inline-block"
                  >
                    <div
                      v-if="getIdentity(data.item.pub_key_stash).logo !== ''"
                      class="d-inline-block"
                    >
                      <img
                        :src="getIdentity(data.item.pub_key_stash).logo"
                        class="identity-small d-inline-block"
                      />
                    </div>
                  </div>
                  <div v-else class="d-inline-block">
                    <Identicon
                      :key="data.item.pub_key_stash"
                      :value="data.item.pub_key_stash"
                      :size="20"
                      :theme="'polkadot'"
                    />
                  </div>
                  <span v-if="hasIdentity(data.item.pub_key_stash)">
                    {{ getIdentity(data.item.pub_key_stash).full_name }}
                  </span>
                  <span v-else-if="hasKusamaIdentity(data.item.pub_key_stash)">
                    {{ getKusamaIdentity(data.item.pub_key_stash).display }}
                  </span>
                  <span v-else>
                    <span
                      class="d-inline d-sm-inline d-md-inline d-lg-inline d-xl-none"
                      >{{ shortAddress(data.item.pub_key_stash) }}</span
                    >
                    <span
                      class="d-none d-sm-none d-md-none d-lg-none d-xl-inline"
                      >{{ shortAddress(data.item.pub_key_stash) }}</span
                    >
                  </span>
                </div>
              </template>
              <template slot="other_stake_count" slot-scope="data">
                <p class="text-right mb-0">
                  {{ data.item.other_stake_count }}
                </p>
              </template>
              <template slot="stake_total" slot-scope="data">
                <p v-if="data.item.stake_total > 0" class="text-right mb-0">
                  {{ formatAmount(data.item.stake_total) }}
                </p>
              </template>
              <template slot="favorite" slot-scope="data">
                <p class="text-center mb-0">
                  <a
                    class="favorite"
                    @click="toggleFavorite(data.item.pub_key_stash)"
                  >
                    <i
                      v-if="data.item.favorite"
                      v-b-tooltip.hover
                      class="fas fa-star"
                      style="color: #f1bd23"
                      :title="$t('pages.phragmen.remove_from_favorites')"
                    />
                    <i
                      v-else
                      v-b-tooltip.hover
                      class="fas fa-star"
                      style="color: #e6dfdf;"
                      :title="$t('pages.phragmen.add_to_favorites')"
                    />
                  </a>
                </p>
              </template>
            </b-table>
            <div style="display: flex">
              <b-pagination
                v-model="currentPage"
                :total-rows="totalRows"
                :per-page="perPage"
                aria-controls="validators-table"
              />
              <b-button-group class="mx-4">
                <b-button
                  v-for="(item, index) in tableOptions"
                  :key="index"
                  @click="handleNumFields(item)"
                >
                  {{ item }}
                </b-button>
              </b-button-group>
            </div>
          </div>
        </template>
      </b-container>
    </section>
  </div>
</template>
<script>
import { mapMutations } from "vuex";
import bootstrap from "bootstrap";
import Identicon from "../components/identicon.vue";
import Network from "../components/network.vue";
import { isHex } from "@polkadot/util";
import BN from "bn.js";
import { blockExplorer, numItemsTableOptions } from "../polkastats.config.js";
import commonMixin from "../mixins/commonMixin.js";

export default {
  components: {
    Identicon
  },
  mixins: [commonMixin],
  data: function() {
    return {
      enabled: true,
      tableOptions: numItemsTableOptions,
      perPage: localStorage.numItemsTableSelected
        ? parseInt(localStorage.numItemsTableSelected)
        : 10,
      currentPage: 1,
      sortBy: `favorite`,
      sortDesc: true,
      filter: null,
      filterOn: [],
      totalRows: 1,
      fields: [
        {
          key: "rank",
          label: "#",
          sortable: true,
          class: `d-none d-sm-none d-md-table-cell d-lg-table-cell d-xl-table-cell`
        },
        {
          key: "pub_key_stash",
          label: this.$i18n.t("pages.phragmen.candidate"),
          sortable: true,
          filterByFormatted: true
        },
        {
          key: "other_stake_count",
          label: this.$i18n.t("pages.phragmen.voters"),
          sortable: true,
          class: `d-none d-sm-none d-md-table-cell d-lg-table-cell d-xl-table-cell`
        },
        {
          key: "stake_total",
          label: this.$i18n.t("pages.phragmen.total_stake"),
          sortable: true,
          class: `d-none d-sm-none d-md-table-cell d-lg-table-cell d-xl-table-cell`
        },
        {
          key: "favorite",
          label: "тнР",
          sortable: true,
          class: `d-none d-sm-none d-md-table-cell d-lg-table-cell d-xl-table-cell`
        }
      ],
      blockExplorer,
      polling: null,
      favorites: []
    };
  },
  computed: {
    network() {
      return this.$store.state.network.info;
    },
    candidates() {
      return this.$store.state.phragmen.info.candidates.map(candidate => {
        if (this.hasKusamaIdentity(candidate.pub_key_stash)) {
          candidate.identity = this.getKusamaIdentity(candidate.pub_key_stash);
        }
        candidate.favorite = this.isFavorite(candidate.pub_key_stash);
        return candidate;
      });
    },
    identities() {
      return this.$store.state.identities.list;
    },
    sortOptions() {
      // Create an options list from our fields
      return this.fields
        .filter(f => f.sortable)
        .map(f => {
          return { text: f.label, value: f.key };
        });
    }
  },
  watch: {
    favorites: function(favorites) {
      this.$cookies.set("favorites", favorites, {
        path: "/",
        maxAge: 60 * 60 * 24 * 7
      });
    }
  },
  created: function() {
    var vm = this;

    // Get favorites from cookie
    if (this.$cookies.get("favorites")) {
      this.favorites = this.$cookies.get("favorites");
    }

    // Force update of phragmen candidates list if empty
    if (this.$store.state.phragmen.info.candidates.length == 0) {
      vm.$store.dispatch("phragmen/update");
    }
    this.totalRows = this.$store.state.phragmen.info.candidates.length;

    // Force update of indentity list if empty
    if (this.$store.state.identities.list.length == 0) {
      vm.$store.dispatch("identities/update");
    }

    // Force update of staking identities list if empty
    if (this.$store.state.stakingIdentities.list.length === 0) {
      vm.$store.dispatch("stakingIdentities/update");
    }

    // Update data every 60 seconds
    this.polling = setInterval(() => {
      vm.$store.dispatch("network/update");
      vm.$store.dispatch("phragmen/update");
      vm.$store.dispatch("identities/update");
      vm.$store.dispatch("stakingIdentities/update");
      if (!this.filter)
        this.totalRows = this.$store.state.phragmen.info.candidates.length;
    }, 60000);
  },
  beforeDestroy: function() {
    clearInterval(this.polling);
  },
  methods: {
    handleNumFields(num) {
      this.perPage = parseInt(num);
    },
    toggleFavorite(accountId) {
      if (this.favorites.indexOf(accountId) !== -1) {
        this.favorites.splice(this.favorites.indexOf(accountId), 1);
      } else {
        this.favorites.push(accountId);
      }
      this.$store.dispatch("phragmen/toogleFavorite", accountId);

      return true;
    },
    isFavorite(accountId) {
      return this.favorites.includes(accountId);
    },
    hasIdentity(stashId) {
      return this.$store.state.identities.list.some(obj => {
        return obj.stashId === stashId;
      });
    },
    getIdentity(stashId) {
      let filteredArray = this.$store.state.identities.list.filter(obj => {
        return obj.stashId === stashId;
      });
      return filteredArray[0];
    },
    hasKusamaIdentity(stashId) {
      return this.$store.state.stakingIdentities.list.some(obj => {
        return obj.accountId === stashId;
      });
    },
    getKusamaIdentity(stashId) {
      let filteredArray = this.$store.state.stakingIdentities.list.filter(
        obj => {
          return obj.accountId === stashId;
        }
      );
      return filteredArray[0].identity;
    },
    onFiltered(filteredItems) {
      // Trigger pagination to update the number of buttons/pages due to filtering
      this.totalRows = filteredItems.length;
      this.currentPage = 1;
    }
  },
  head() {
    return {
      title: this.$t("pages.phragmen.head_title"),
      meta: [
        {
          hid: "description",
          name: "description",
          content: this.$t("pages.phragmen.head_content")
        }
      ]
    };
  }
};
</script>
